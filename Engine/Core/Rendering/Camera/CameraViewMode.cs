namespace Staple;

/// <summary>
/// Camera view mode
/// </summary>
public enum CameraViewMode
{
    /// <summary>
    /// Default
    /// </summary>
    Default,

    /// <summary>
    /// Sequential (first to last)
    /// </summary>
    Sequential,

    /// <summary>
    /// Sort by depth (ascending)
    /// </summary>
    DepthAscending,

    /// <summary>
    /// Sort by depth (descending)
    /// </summary>
    DepthDescending,
}
